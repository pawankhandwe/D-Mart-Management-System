    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        if(updatetable.getSelectedRow()==-1)
        {
            JOptionPane.showMessageDialog(null,"please select a row","incomplete",JOptionPane.ERROR_MESSAGE);
             return;
        }
        if(!validateinput())
        {
          JOptionPane.showMessageDialog(null,"please fill all the fields","incomplete",JOptionPane.ERROR_MESSAGE);
             return;
        }
        try
        {
            int n=txttax.getSelectedItem().toString().length();
            int tax=Integer.parseInt(txttax.getSelectedItem().toString().substring(0,n-1));
            ProductsPojo p=new ProductsPojo();
            p.setProductid(txtid.getText());
            p.setProductname(txtname.getText());
            p.setProductcompany(txtcompany.getText());
            p.setProductprice(Double.parseDouble(txtprice1.getText()));
            p.setOurprice(Double.parseDouble(txtprice.getText()));
            p.setQuantity(Integer.parseInt(txtquantity.getText()));
            p.setTax(tax);
            boolean res=ProductDao.updateproducts(p);
            if(res)
            {
                JOptionPane.showMessageDialog(null,"products updated sucessfully","updated",JOptionPane.INFORMATION_MESSAGE);
          dm.setRowCount(0);
          getalldetails();
           btnupdate.setEnabled(false);
                return;
            }
          JOptionPane.showMessageDialog(null,"products not updated ","error",JOptionPane.ERROR_MESSAGE);
            
            
        }catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null,"error in db","incomplete",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null,"please fill numberic value","incomplete",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnupdateActionPerformed


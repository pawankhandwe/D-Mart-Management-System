 private void getalldetails() {
        
        try{
            List<ProductsPojo>productdetails=ProductDao.getProductsDetails();
            if(productdetails.isEmpty())
            {
             JOptionPane.showMessageDialog(null,"no products present in stock","incomplete",JOptionPane.ERROR_MESSAGE);
           return;
            }
            dm=(DefaultTableModel)updatetable.getModel();
            Object[]rows=new Object[7];
            for(ProductsPojo product:productdetails)
            {
                rows[0]=product.getProductid();
                rows[1]=product.getProductname();
                rows[2]=product.getProductcompany();
                rows[3]=product.getProductprice();
                rows[4]=product.getOurprice();
                rows[5]=product.getQuantity();
                rows[6]=product.getTax();
                dm.addRow(rows);
            }
             
        }catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null,"error in db","incomplete",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
        }
